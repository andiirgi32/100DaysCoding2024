package SeratusHariJava;

public class Day35 {

    public static void main(String[] args) {
        //Tugas Operator Aritmatika : Penjumlahan & Pengurangan
        
        int a = 28, b = 26;
        
        // Penjumlahan
        System.out.println("Hasil dari "+a+" + "+b+" = "+(a+b));
        
        // Pengurangan
        System.out.println("Hasil dari "+a+" - "+b+" = "+(a-b));
    }
}
