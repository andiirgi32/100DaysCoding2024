package SeratusHariJava;

public class Day8 {
    public static void main(String[] args) {
        // Tugas Pembuatan Variabel Update
        
        // Memasukan nilai awal variabel
        String nama = "A. Irgi Irwan. A";
        String pesan = "Halo! Apa Kabar";
        String lokasi = "Kec Campalagian";
        
        // Menampilkan nilai awal variiabel
        System.out.println("Nama         : "+nama);
        System.out.println("Pesan        : "+pesan);
        System.out.println("Lokasi Pesan : "+lokasi);
        
        System.out.println("");
        System.out.println("=========================");
        System.out.println("");
        
        // Mengubah(Update) nilai variabel
        nama = "irgi";
        pesan = "Tunjukkan! Ku ingin bertemu";
        lokasi = "Kab Majene";
        
        // Menampilkan update nilai variiabel
        System.out.println("Nama         : "+nama);
        System.out.println("Pesan        : "+pesan);
        System.out.println("Lokasi Pesan : "+lokasi);
    }
}
