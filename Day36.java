package SeratusHariJava;

public class Day36 {

    public static void main(String[] args) {
        //Tugas Operator Aritmatika : Perkalian, Pembagian serta Modulo
        
        int a = 28, b = 14;
        
        int Perkalian = a*b;
        double Pembagian = a/b;
        int Modulo = a%b;
        
        // Perkalian
        System.out.println("Hasil dari "+a+" x "+b+" = "+Perkalian);
        
        // Pembagian
        System.out.println("Hasil dari "+a+" / "+b+" = "+Pembagian);
        
        // Modulo
        System.out.println("Hasil dari "+a+" % "+b+" = "+Modulo);
    }
}
