package SeratusHariJava;

public class Day38 {

    public static void main(String[] args) {
        //Tugas Operator Penugasan : Pengisian dan Perkalian, Pengisian dan Pembagian
        
        int nilai = 9; // Pengisian Nilai
        
        System.out.println("Hasil Pengisian : "+nilai);
        
        nilai *= 4; // Pengisian dan perkalian
        System.out.println("");
        System.out.println("Proses perkalian nilai");
        System.out.println("Hasil pengisian dan perkalian : "+nilai);
        
        nilai /= 2; // Pengisian dan pembagian
        System.out.println("");
        System.out.println("Proses pembagian nilai");
        System.out.println("Hasil pengisian dan pembagian : "+nilai);
    }
}
